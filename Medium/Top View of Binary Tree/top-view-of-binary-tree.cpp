void TopView(Node* root , int bf, map<int,vector<int>>&mp , int level){
    if (root==NULL) return;
    if(mp.find(bf)==mp.end()){
        vector<int>v;
        v.push_back(level);
        v.push_back(root->data);
        mp[bf]=v;
    }
    if(mp[bf][0]>level){
        mp[bf][0]=level;
        mp[bf][1]=root->data;
    }
    TopView(root->left,bf-1,mp,level+1);
    TopView(root->right,bf+1,mp,level+1);
}
class Solution
{
    public:
    vector<int> topView(Node *root)
    {
        map<int,vector<int>>mp;
        vector<int>v1;
        int bf=0,level=0;
        TopView(root,bf,mp,level);
        for(auto it:mp){
            v1.push_back(it.second[1]);
        }
        return v1;
    }

};



